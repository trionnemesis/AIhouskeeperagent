# ── dialect 封裝 ───────────────────────────────────────────────
def _dialect(conn) -> str:
    return "postgres" if type(conn).__module__.split(".")[0] == "psycopg" else "sqlite"


def _pp(dialect: str) -> str:
    """positional placeholder。"""
    return "%s" if dialect == "postgres" else "?"


def _ph(dialect: str, name: str) -> str:
    """named placeholder。"""
    return f"%({name})s" if dialect == "postgres" else f":{name}"


def _build_insert(dialect: str, table: str, cols: list[str], conflict: list[str]) -> str:
    placeholders = ", ".join(_ph(dialect, c) for c in cols)
    collist = ", ".join(cols)
    if dialect == "sqlite":
        return f"INSERT OR IGNORE INTO {table} ({collist}) VALUES ({placeholders})"
    return (f"INSERT INTO {table} ({collist}) VALUES ({placeholders}) "
            f"ON CONFLICT ({', '.join(conflict)}) DO NOTHING")


def _exec_many(conn, dialect: str, sql: str, params: list[dict]) -> int:
    """executemany → 回實際新增筆數（去重後）。"""
    if dialect == "sqlite":
        before = conn.total_changes
        conn.executemany(sql, params)
        return conn.total_changes - before
    with conn.cursor() as cur:
        cur.executemany(sql, params)
        return cur.rowcount if (cur.rowcount or 0) >= 0 else len(params)


# ── 連線 / schema ──────────────────────────────────────────────
def connect(url: str):
    """SQLite 路徑/':memory:'（預設）或 'postgresql://...'（Cloud SQL）。"""
    if url.startswith(("postgres://", "postgresql://")):
        import psycopg
        from psycopg.rows import dict_row
        return psycopg.connect(url, row_factory=dict_row)
    if url != ":memory:":
        Path(url).expanduser().resolve().parent.mkdir(parents=True, exist_ok=True)
    conn = sqlite3.connect(url)
    conn.row_factory = sqlite3.Row
    return conn


def init_schema(conn) -> None:
    if _dialect(conn) == "sqlite":
        conn.executescript(_SQLITE_SCHEMA)
    else:
        for stmt in _PG_SCHEMA.split(";"):
            if stmt.strip():
                conn.execute(stmt)
    conn.commit()


# ── provenance（DI-7）─────────────────────────────────────────
def record_provenance(conn, entity_type: str, entity_ref: str, source: str,
                      authority: str, confidence: float, as_of: str, recorded_at: str) -> None:
    d = _dialect(conn)
    cols = ["entity_type", "entity_ref", "source", "authority", "confidence", "as_of", "recorded_at"]
    sql = (f"INSERT INTO provenance ({', '.join(cols)}) "
           f"VALUES ({', '.join(_ph(d, c) for c in cols)})")
    conn.execute(sql, {"entity_type": entity_type, "entity_ref": entity_ref, "source": source,
                       "authority": authority, "confidence": confidence, "as_of": as_of,
                       "recorded_at": recorded_at})


def query_provenance(conn, entity_type: str) -> list[dict]:
    pp = _pp(_dialect(conn))
    cur = conn.execute(f"SELECT * FROM provenance WHERE entity_type={pp} ORDER BY id", (entity_type,))
    return [dict(r) for r in cur.fetchall()]


# ── lvr_trades ────────────────────────────────────────────────
def upsert_lvr_trades(conn, rows: list[dict], source: str, data_as_of: str, ingested_at: str) -> int:
    d = _dialect(conn)
    cols = ["district", "deal_type", "building_type", "trade_date", "total_price",
            "unit_price_net", "area_ping", "parking_price", "project_name",
            "source", "data_as_of", "ingested_at"]
    sql = _build_insert(d, "lvr_trades", cols, ["district", "trade_date", "total_price", "area_ping"])
    params = [{
        "district": r["district"], "deal_type": r.get("deal_type", "sale"),
        "building_type": r.get("building_type", ""), "trade_date": r["trade_date"],
        "total_price": r.get("total_price"), "unit_price_net": r.get("unit_price_net"),
        "area_ping": r.get("area_ping"), "parking_price": r.get("parking_price", 0),
        "project_name": r.get("project_name"), "source": source,
        "data_as_of": data_as_of, "ingested_at": ingested_at,
    } for r in rows]
    added = _exec_many(conn, d, sql, params)
    record_provenance(conn, "lvr_trade", data_as_of, source, "gov", 1.0, data_as_of, ingested_at)
    conn.commit()
    return added


def query_lvr_comps(conn, district: str) -> list[dict]:
    pp = _pp(_dialect(conn))
    cur = conn.execute(f"SELECT * FROM lvr_trades WHERE district={pp} ORDER BY trade_date", (district,))
    return [dict(r) for r in cur.fetchall()]


