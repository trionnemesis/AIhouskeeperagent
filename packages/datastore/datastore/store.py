# ── traffic_accidents（DI-5：點位入庫供聚合，tool 不回個別點）──────
def _haversine_m(lat1: float, lng1: float, lat2: float, lng2: float) -> float:
    r = 6_371_000.0
    p1, p2 = math.radians(lat1), math.radians(lat2)
    dp, dl = math.radians(lat2 - lat1), math.radians(lng2 - lng1)
    a = math.sin(dp / 2) ** 2 + math.cos(p1) * math.cos(p2) * math.sin(dl / 2) ** 2
    return 2 * r * math.asin(math.sqrt(a))


def upsert_traffic_accidents(conn, rows: list[dict], source: str, as_of: str, ingested_at: str) -> int:
    d = _dialect(conn)
    cols = ["lat", "lng", "severity", "occurred_at", "source", "as_of", "ingested_at"]
    sql = _build_insert(d, "traffic_accidents", cols, ["lat", "lng", "severity", "occurred_at"])
    params = [{
        "lat": r["lat"], "lng": r["lng"], "severity": r["severity"],
        "occurred_at": r["occurred_at"], "source": source, "as_of": as_of, "ingested_at": ingested_at,
    } for r in rows]
    added = _exec_many(conn, d, sql, params)
    record_provenance(conn, "traffic_accident", as_of, source, "gov", 1.0, as_of, ingested_at)
    conn.commit()
    return added


def query_accident_points_near(conn, lat: float, lng: float, radius_m: float) -> list[dict]:
    """半徑內事故點（bbox 粗篩 + haversine 精篩）。**內部用**：呼叫端須聚合後輸出（DI-5）。"""
    pp = _pp(_dialect(conn))
    deg_lat = radius_m / 111_320.0
    deg_lng = radius_m / (111_320.0 * max(math.cos(math.radians(lat)), 1e-6))
    cur = conn.execute(
        f"SELECT lat, lng, severity, occurred_at FROM traffic_accidents "
        f"WHERE lat BETWEEN {pp} AND {pp} AND lng BETWEEN {pp} AND {pp}",
        (lat - deg_lat, lat + deg_lat, lng - deg_lng, lng + deg_lng),
    )
    out: list[dict] = []
    for r in cur.fetchall():
        if _haversine_m(lat, lng, r["lat"], r["lng"]) <= radius_m:
            out.append(dict(r))
    return out
